<?php
//=======================================================================================
//データベースの情報
//========================================================================================
$dsn='データベース名';
$user='ユーザー名';
$password='パスワード';

try{
//========================================================================================
//データベース接続
//=========================================================================================
$pdo=new PDO($dsn,$user,$password);
print('接続に成功しました。<br>');

//==========================================================================================
//テーブル作成
//============================================================================================
$sql = "CREATE TABLE IF NOT EXISTS nameData(".
//ID：連番のINT型
"ID INT NOT NULL auto_increment primary key,".
//NAME:名前
"NAME char(15),".
//COMMENT:コメント
"COMMENT text,".
//day:日付
"day DATETIME".
");";
$stmt = $pdo -> prepare($sql);
$stmt -> execute();
}
//================================================================
//接続エラー
//==================================================================
catch(PDOException $e){
   print('エラーが発生しました。'.$e->getMessage());
   die();
}

?>
     