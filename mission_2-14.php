<?php
//=======================================================================================
//データベースの情報
//========================================================================================
$dsn='データベース名';
$user='ユーザー名';
$password='パスワード';

try{
//========================================================================================
//データベース接続
//=========================================================================================
$pdo=new PDO($dsn,$user,$password);

//==========================================================================================
//テーブルデータ削除
//============================================================================================

//DELETE文を変数に格納
$sql="DELETE FROM nameData where ID=:id";

//☆知識：executeを使う時は、prepareを使わなくてはいけない。
$result=$pdo->prepare($sql);

//更新データを格納
$edit=array(':id'=>'1');

$result->execute($edit);

echo "削除完了";

}//try閉じ
//================================================================
//接続エラー
//==================================================================
catch(PDOException $e){
   print('エラーが発生しました。'.$e->getMessage());
   die();
}
?>
   